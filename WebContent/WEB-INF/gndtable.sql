-- M_ARTIST 의 정보를 저장할 테이블
CREATE TABLE M_ARTIST(
	ARTIST_ID NUMBER PRIMARY KEY,
	ARTIST_NAME VARCHAR2(255) NOT NULL,
	ARTIST_E_NAME VARCHAR2(255) NOT NULL,
