-- M_PLAYLIST 의 정보를 저장할 테이블
CREATE TABLE M_PLAYLIST(
	PLAYLIST_ID NUMBER PRIMARY KEY,
	PLAYLIST_WRITER VARCHAR2(255) NOT NULL,
	PLAYLIST_TITLE VARCHAR2(255) NOT NULL,
	PLAYLIST_LINK CLOB NOT NULL,
